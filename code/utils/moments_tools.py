def invariant_feature_distance(
    feat_a: Dict[str, float],
    feat_b: Dict[str, float],
    metric: str = "euclidean",
    inv_cov: Optional[np.ndarray] = None
) -> float:

    common_keys = sorted(set(feat_a.keys()) & set(feat_b.keys()))
    if not common_keys:
        raise ValueError("No shared keys to compare.")

    vec_a = np.array([feat_a[k] for k in common_keys])
    vec_b = np.array([feat_b[k] for k in common_keys])

    if metric == "euclidean":
        return np.linalg.norm(vec_a - vec_b)

    elif metric == "cosine":
        norm_a = np.linalg.norm(vec_a)
        norm_b = np.linalg.norm(vec_b)
        if norm_a == 0 or norm_b == 0:
            return 1.0
        return 1 - np.dot(vec_a, vec_b) / (norm_a * norm_b)

    elif metric == "mahalanobis":
        if inv_cov is None:
            raise ValueError("inv_cov is required for Mahalanobis distance.")
        return mahalanobis(vec_a, vec_b, inv_cov)

    else:
        raise ValueError(f"Unsupported metric: {metric}")


